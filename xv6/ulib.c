char*
gets(char *buf, int max)
{
  int i, cc;
  char c;

  for(i=0; i+1 < max; ){
    cc = read(0, &c, 1);
    if(cc < 1)
      break;
    buf[i++] = c;
    if(c == '\n' || c == '\r')
      break;
  }
  buf[i] = '\0';
  return buf;
}

int
stat(char *n, struct stat *st)
{
  int fd;
  int r;

  fd = open(n, O_RDONLY);
  if(fd < 0)
    return -1;
  r = fstat(fd, st);
  close(fd);
  return r;
}

int
atoi(const char *s)
{
  int n;

  n = 0;
  while('0' <= *s && *s <= '9')
    n = n*10 + *s++ - '0';
  return n;
}

void*
memmove(void *vdst, void *vsrc, int n)
{
  char *dst, *src;
  
  dst = vdst;
  src = vsrc;
  while(n-- > 0)
    *dst++ = *src++;
  return vdst;
}

char*
concat(char *str1, char *str2, char *buffer)
{
  int len1 = strlen(str1);
  int len2 = strlen(str2);
  strcpy(buffer, str1);
  strcpy(buffer + len1, str2);

  buffer[len1 + len2 ] = '\0';

  return buffer;
}

// itoa
// modified version of code supplied at
// http://opensource.apple.com//source/groff/groff-12/groff/libgroff/itoa.c
char*
itoa(int i, char *buffer, int len)
{
  
  char *p = buffer + len - 1; /* points to terminating '\0' */
  *p = '\0';
  if (i >= 0) {
    do {
      *--p = '0' + (i % 10);
      i /= 10;
    } while (i != 0);
    return p;
  
  } else {      /* i < 0 */
    do {
      *--p = '0' - (i % 10);
        i /= 10;
    } while (i != 0);
      *--p = '-';
      }
  return p;
}